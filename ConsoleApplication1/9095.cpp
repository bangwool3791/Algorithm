#include "pch.h"

int dp[1001];

int main()
{
	int n = 0;
	cin >> n;

	int a = 0;

	dp[0] = 1;
	dp[1] = 1;
	dp[2] = 2;

	while (n--)
	{
		cin >> a;

		for (int i = 3; i <= a; ++i)
		{
			dp[i] = dp[i - 1] + dp[i - 2] + dp[i - 3];
		}
		cout << dp[a] << endl;
	}

	return 0;
}

